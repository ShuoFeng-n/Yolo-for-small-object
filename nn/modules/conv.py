class Inception_block_v3(nn.Module):
    def __init__(self,input1 =128,n1x1=64, n3x3_reduce=48, n3x3=96, n5x5_reduce=32, n5x5=48, pool_proj=48):
        super(Inception_block_v3, self).__init__()
